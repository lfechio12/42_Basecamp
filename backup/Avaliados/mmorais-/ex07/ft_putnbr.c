#include <unistd.h>

void	ft_putnbr(int nb)
{
	int a;

	if (nb <= 0)
	{
		write(1, "-", 1);
		a = nb * (-1) - 1;
	}
	else
		a = nb;
	if (a >= 10)
		ft_putnbr(a / 10);
	if (nb <= 0)
	{
		a = a % 10 + 49;
		write(1, &a, 1);
	}
	else
	{
		a = a % 10 + 48;
		write(1, &a, 1);
	}
}
